package com.vuvrecharge.modules.model;

public class SpinnerData {
    String id;
    String value;

    public SpinnerData() {
    }

    public SpinnerData(String id, String value) {
        this.id = id;
        this.value = value;
    }

    public String getId() {
        return id;
    }

    public String getValue() {
        return value;
    }
}
