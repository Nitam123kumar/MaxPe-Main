package com.vuvrecharge.modules.model;

public class Subscriptions {
    String ott_name;
    String ott_desc;
    String logo;

    public String getOtt_name() {
        return ott_name;
    }

    public void setOtt_name(String ott_name) {
        this.ott_name = ott_name;
    }

    public String getOtt_desc() {
        return ott_desc;
    }

    public void setOtt_desc(String ott_desc) {
        this.ott_desc = ott_desc;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }
}
